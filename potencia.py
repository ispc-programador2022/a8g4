"""
Creación de una función que retorna la potencia del primero elevado al segundo parámetro.
"""
base = float(input("Por favor ingrese el número base: "))  # se introduce el número base
exponente = float(input("Por favor ingrese el exponente: ")) # se introduce el exponente
potencia = base ** exponente # operación
print ("El resultado de la potencia de base", base, "con exponente", exponente, "es:", potencia) # impresión del resultado
